a = input('Digite qualquer coisa: ')
print('O tipo primitivo desse valor é', type(a),
'\nÉ alpha númerico?',     a.isalpha(),
'\nÉ um espaço?',      a.isspace(),
'\nÉ minúsculo?',     a.islower(),
'\nÉ maiúsculo?',      a.isupper,
'\nÉ possível printar?',      a.isprintable(),
'\nÉ númerico?',     a.isnumeric(),
'\nÉ decimal?',     a.isdecimal(),)